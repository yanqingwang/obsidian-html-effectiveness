import esbuild from "esbuild";
import process from "process";

const banner = `/*
THIS IS A GENERATED BUNDLE FOR OBSIDIAN PLUGIN.
DO NOT EDIT THIS FILE DIRECTLY.
*/
`;

const prod = process.argv[2] === "production";

const context = await esbuild.context({
	banner: { js: banner },
	entryPoints: ["main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		// Node built-in modules (explicit list to avoid builtin-modules dependency)
		"assert", "async_hooks", "buffer", "child_process", "cluster", "console",
		"constants", "crypto", "dgram", "dns", "domain", "events", "fs", "http",
		"http2", "https", "inspector", "module", "net", "os", "path",
		"perf_hooks", "process", "punycode", "querystring", "readline", "repl",
		"stream", "string_decoder", "timers", "tls", "trace_events", "tty",
		"url", "util", "v8", "vm", "wasi", "worker_threads", "zlib",
	],
	format: "cjs",
	target: "es2020",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "main.js",
});

if (prod) {
	await context.rebuild();
	process.exit(0);
} else {
	await context.watch();
}
